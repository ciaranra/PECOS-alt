impl<S: IndexSet, R: SeedableRng + Rng + Debug> CliffordGateable for CHFormGeneric<S, R> {
    fn sz(&mut self, qubits: &[QubitId]) -> &mut Self {
        for &q in qubits {
            self.apply_s(q.index());
        }
        self
    }

    fn h(&mut self, qubits: &[QubitId]) -> &mut Self {
        for &q in qubits {
            self.apply_h(q.index());
        }
        self
    }

    fn cx(&mut self, pairs: &[(QubitId, QubitId)]) -> &mut Self {
        for &(q0, q1) in pairs {
            self.apply_cx(q0.index(), q1.index());
        }
        self
    }

    fn mz(&mut self, qubits: &[QubitId]) -> Vec<MeasurementResult> {
        qubits
            .iter()
            .map(|&q| self.apply_mz(q.index(), None))
            .collect()
    }

    fn x(&mut self, qubits: &[QubitId]) -> &mut Self {
        // X_q on CH-form: commute X through U_C to get Pauli product on |s⟩.
        // U_C^{-1} X_q U_C maps |x⟩ → i^{ΔQ(x)} |x ⊕ g⟩ where g = G[q,:].
        //
        // The Pauli product: (∏_{j∈g} X_j) · Z-phase.
        // X_j on v-qubit: phase (-1)^{s_j} (X H|s_j⟩ = (-1)^{s_j} H|s_j⟩)
        // X_j on non-v qubit: toggle s_j (X|s_j⟩ = |1-s_j⟩)
        // Z part from M and gamma: Z_j contributes (-1)^{s_j} for non-v, toggles s for v.
        //
        // Full formula derived from Q(x) - Q(x⊕g):
        // phase = i^{Σ_{j∈g} γ_j} · (-1)^{Σ_{j∈g} Σ_{k∈g,k<j} M_{jk}}
        //       · Z-operator from M cross-terms with non-g qubits.
        //
        // X = H*Z*H in the CH-form picture.
        // TODO: derive direct formula for O(|G[q,:]|) implementation.
        for &q in qubits {
            self.apply_h(q.index());
            self.apply_z(q.index());
            self.apply_h(q.index());
        }
        self
    }

    fn szdg(&mut self, qubits: &[QubitId]) -> &mut Self {
        // Sdg = Z * S: gamma[q] += 2 + apply_s
        for &q in qubits {
            self.apply_z(q.index());
            self.apply_s(q.index());
        }
        self
    }

    fn z(&mut self, qubits: &[QubitId]) -> &mut Self {
        for &q in qubits {
            self.apply_z(q.index());
        }
        self
    }

    fn cz(&mut self, pairs: &[(QubitId, QubitId)]) -> &mut Self {
        for &(q0, q1) in pairs {
            self.apply_cz(q0.index(), q1.index());
        }
        self
    }

    fn szz(&mut self, pairs: &[(QubitId, QubitId)]) -> &mut Self {
        // SZZ = CZ * S * S * phase. Batch M updates in single make_mut.
        for &(q0, q1) in pairs {
            let q = q0.index();
            let r = q1.index();
            // CZ: M[q,:] ^= G[r,:], M[r,:] ^= G[q,:]
            // S(q): M[q,:] ^= G[q,:], gamma[q] -= 1
            // S(r): M[r,:] ^= G[r,:], gamma[r] -= 1
            let m = std::sync::Arc::make_mut(&mut self.m);
            m.row_xor_from(q, &self.g, r); // CZ part 1
            m.row_xor_from(r, &self.g, q); // CZ part 2
            m.row_xor_from(q, &self.g, q); // S(q)
            m.row_xor_from(r, &self.g, r); // S(r)
            self.gamma[q] = (self.gamma[q] + 3) & 3;
            self.gamma[r] = (self.gamma[r] + 3) & 3;
            self.omega.mul_phase(1);
        }
        self
    }

    fn szzdg(&mut self, pairs: &[(QubitId, QubitId)]) -> &mut Self {
        // SZZdg = CZ * Sdg * Sdg * phase. Sdg = Z*S.
        for &(q0, q1) in pairs {
            let q = q0.index();
            let r = q1.index();
            let m = std::sync::Arc::make_mut(&mut self.m);
            m.row_xor_from(q, &self.g, r); // CZ
            m.row_xor_from(r, &self.g, q);
            m.row_xor_from(q, &self.g, q); // S(q) part of Sdg
            m.row_xor_from(r, &self.g, r); // S(r) part of Sdg
            // Sdg gamma: Z adds +2, S adds +3 = total +5 = +1 mod 4
            self.gamma[q] = (self.gamma[q] + 1) & 3;
            self.gamma[r] = (self.gamma[r] + 1) & 3;
            self.omega.mul_phase(7);
        }
        self
    }

    fn sxx(&mut self, pairs: &[(QubitId, QubitId)]) -> &mut Self {
        // SXX = CX * (H*S*H) * CX * phase. Faster than 4H for standalone SXX.
        for &(q0, q1) in pairs {
            let q = q0.index();
            let r = q1.index();
            self.apply_cx(q, r);
            self.apply_h(q);
            self.apply_s(q);
            self.apply_h(q);
            self.apply_cx(q, r);
            self.omega.mul_phase(7);
        }
        self
    }

    fn sxxdg(&mut self, pairs: &[(QubitId, QubitId)]) -> &mut Self {
        // SXXdg = CX * (H*Sdg*H) * CX * e^{i*pi/4}
        for &(q0, q1) in pairs {
            let q = q0.index();
            let r = q1.index();
            self.apply_cx(q, r);
            self.apply_h(q);
            self.apply_szdg(q);
            self.apply_h(q);
            self.apply_cx(q, r);
            self.omega.mul_phase(1);
        }
        self
    }

    fn syy(&mut self, pairs: &[(QubitId, QubitId)]) -> &mut Self {
        // SYY = S*S * (H*H*SZZ*H*H) * Sdg*Sdg
        // Uses H-based SXX decomposition for better state structure.
        let all_qubits: Vec<QubitId> = pairs.iter().flat_map(|&(q0, q1)| [q0, q1]).collect();
        self.sz(&all_qubits);
        self.h(&all_qubits);
        self.szz(pairs);
        self.h(&all_qubits);
        self.szdg(&all_qubits);
        self
    }

    fn syydg(&mut self, pairs: &[(QubitId, QubitId)]) -> &mut Self {
        // SYYdg = S*S * (H*H*SZZdg*H*H) * Sdg*Sdg
        let all_qubits: Vec<QubitId> = pairs.iter().flat_map(|&(q0, q1)| [q0, q1]).collect();
        self.sz(&all_qubits);
        self.h(&all_qubits);
        self.szzdg(pairs);
        self.h(&all_qubits);
        self.szdg(&all_qubits);
        self
    }

    fn mnz(&mut self, qubits: &[QubitId]) -> Vec<MeasurementResult> {
        // Measure -Z: X conjugation. X*Z*X = -Z.
        // Default uses X (2H). Can't avoid H for non-Z measurement.
        self.x(qubits);
        let results = self.mz(qubits);
        self.x(qubits);
        results
    }

    fn cy(&mut self, pairs: &[(QubitId, QubitId)]) -> &mut Self {
        // CY = Sdg(tgt) * CX * S(tgt). All C-type.
        // Sdg = Z*S: gamma[r] += 1 (Z+2 + S+3 = +5 = +1 mod 4), M[r,:] ^= G[r,:]
        // CX: gamma[q] += gamma[r] + 2*ip, G[r,:] ^= G[q,:], F[q,:] ^= F[r,:], M[q,:] ^= M[r,:]
        // S: gamma[r] += 3, M[r,:] ^= G[r,:]
        for &(q0, q1) in pairs {
            let q = q0.index();
            let r = q1.index();
            // Sdg(r): gamma[r] += 1, M[r,:] ^= G[r,:]
            self.gamma[r] = (self.gamma[r] + 1) & 3;
            std::sync::Arc::make_mut(&mut self.m).row_xor_from(r, &self.g, r);
            // CX(q,r)
            let ip = self.m.row(q).intersection_count(self.f.row(r)) & 1;
            #[allow(clippy::cast_possible_truncation)] // ip masked to 0 or 1
            {
                self.gamma[q] = (self.gamma[q] + self.gamma[r] + 2 * (ip as u8)) & 3;
            }
            std::sync::Arc::make_mut(&mut self.g).row_xor_assign(r, q);
            std::sync::Arc::make_mut(&mut self.f).row_xor_assign(q, r);
            std::sync::Arc::make_mut(&mut self.m).row_xor_assign(q, r);
            // S(r): gamma[r] += 3, M[r,:] ^= G[r,:]
            self.gamma[r] = (self.gamma[r] + 3) & 3;
            std::sync::Arc::make_mut(&mut self.m).row_xor_from(r, &self.g, r);
        }
        self
    }
}

