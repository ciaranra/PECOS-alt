// ============================================================================
// StabilizerTableauSimulator -- compute stabilizer generators from CH-form
// ============================================================================

use crate::stabilizer_tableau::StabilizerTableauSimulator;

impl<S: IndexSet, R: SeedableRng + Rng + Debug> StabilizerTableauSimulator for CHFormGeneric<S, R> {
    fn stab_tableau(&self) -> String {
        // Compute stabilizer generators from the CH-form.
        // The stabilizer of |psi> = omega * U_C * U_H * |s> consists of:
        //
        // For v[j]=0: stabilizer = (-1)^{s[j]} * Z(G[j,:])
        //   (Z conjugation by C-type Cliffords has no phase)
        //
        // For v[j]=1: stabilizer = (-1)^{s[j]} * U_C * X_j * U_C^{-1}
        //   The paper gives U_C^{-1} X_j U_C = i^{gamma[j]} X(F[j,:]) Z(M[j,:])
        //   The inverse (what we need) is:
        //     U_C X_j U_C^{-1} = i^{-gamma[j]} * (-1)^{F[j,:].M[j,:]} * X(F[j,:]) Z(M[j,:])
        //   Combined sign: (-1)^{s[j]} * i^{-gamma[j]} * (-1)^{F.M}
        let n = self.num_qubits;
        let mut lines = Vec::with_capacity(n);

        for j in 0..n {
            let mut pauli = vec!['I'; n];

            if self.v.contains(j) {
                // Compute the Pauli string from F and M.
                // The raw operator is X(F[j,:]) Z(M[j,:]).
                // On sites where both X=1 and Z=1, XZ = -iY, so each Y-site
                // contributes a factor of (-i) = i^3 to the overall phase.
                let mut y_count = 0u8;
                for (k, p) in pauli.iter_mut().enumerate() {
                    let x = self.f.get(j, k);
                    let z = self.m.get(j, k);
                    *p = match (x, z) {
                        (false, false) => 'I',
                        (true, false) => 'X',
                        (false, true) => 'Z',
                        (true, true) => {
                            y_count += 1;
                            'Y'
                        }
                    };
                }

                // Phase of the stabilizer:
                //   U_C X_j U_C^{-1} = (U_C^{-1} X_j U_C)^{-1}
                // where U_C^{-1} X_j U_C = i^{gamma[j]} * X(F) * Z(M)
                // and X(F)Z(M) = (-i)^{y_count} * (Pauli string with Y's)
                //
                // So U_C^{-1} X_j U_C = i^{gamma[j]} * (-i)^{y_count} * P
                //    where P is the displayed Pauli string.
                // Its inverse: P^{-1} = P (Paulis are self-inverse), so
                //   (i^{gamma} * (-i)^{y_count} * P)^{-1}
                //     = i^{-gamma} * (i)^{y_count} * P
                //     (since (-i)^{-1} = i)
                //
                // Full stabilizer: (-1)^{s[j]} * i^{-gamma[j]} * i^{y_count} * P
                let neg_gamma = (4 - self.gamma[j]) & 3;
                let s_bit = u8::from(self.s.contains(j));
                // i^{-gamma} * i^{y_count} * (-1)^{s} = i^{(-gamma + y_count + 2*s) mod 4}
                let total_i_pow = (neg_gamma + (y_count & 3) + 2 * s_bit) & 3;

                let sign = match total_i_pow {
                    0 => "+",
                    1 => "+i",
                    2 => "-",
                    3 => "-i",
                    _ => unreachable!(),
                };

                let pauli_str: String = pauli.into_iter().collect();
                lines.push(format!("{sign}{pauli_str}"));
            } else {
                // Stabilizer is (-1)^{s[j]} * Z(G[j,:])
                for k in self.g.row(j).iter() {
                    pauli[k] = 'Z';
                }
                let sign = if self.s.contains(j) { "-" } else { "+" };
                let pauli_str: String = pauli.into_iter().collect();
                lines.push(format!("{sign}{pauli_str}"));
            }
        }
        lines.join("\n")
    }

    fn destab_tableau(&self) -> String {
        // Destabilizers are the anti-commuting partners.
        // For now, provide a basic implementation.
        let n = self.num_qubits;
        let mut lines = Vec::with_capacity(n);

        for j in 0..n {
            let mut pauli = vec!['I'; n];

            if self.v.contains(j) {
                // Destabilizer for X-stabilizer is Z-type
                for k in self.g.row(j).iter() {
                    pauli[k] = 'Z';
                }
            } else {
                // Destabilizer for Z-stabilizer is X-type
                for (k, p) in pauli.iter_mut().enumerate() {
                    let x = self.f.get(j, k);
                    let z = self.m.get(j, k);
                    *p = match (x, z) {
                        (false, false) => 'I',
                        (true, false) => 'X',
                        (false, true) => 'Z',
                        (true, true) => 'Y',
                    };
                }
            }

            let pauli_str: String = pauli.into_iter().collect();
            lines.push(format!("+{pauli_str}"));
        }
        lines.join("\n")
    }

    fn num_qubits(&self) -> usize {
        self.num_qubits
    }
}

