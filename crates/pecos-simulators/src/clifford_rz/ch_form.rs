    /// Helper: create a CH-form state with H on all qubits, CX chain, then Z on q0.
    /// Returns (state1, state2) where state2 has Z applied.
    fn make_test_pair(n: usize) -> (CHForm, CHForm) {
        let mut ch1 = CHForm::new_with_seed(n, 42);
        for q in 0..n {
            ch1.h(&[QubitId(q)]);
        }
        if n > 1 {
            ch1.cx(&[(QubitId(0), QubitId(1))]);
        }
        let mut ch2 = ch1.clone();
        ch2.z(&[QubitId(0)]);
        (ch1, ch2)
    }

    #[test]
    fn test_inner_product_pair_vs_separate() {
        // Verify inner_product_pair gives the same results as calling
        // inner_product + inner_product_z separately.
        for n in [1, 2, 5, 10, 30, 62, 63, 64, 127, 150] {
            let mut ch1 = CHForm::new_with_seed(n, 42);
            let mut ch2 = CHForm::new_with_seed(n, 99);
            for q in 0..n {
                ch1.h(&[QubitId(q)]);
            }
            if n > 1 {
                ch1.cx(&[(QubitId(0), QubitId(1))]);
            }
            ch1.sz(&[QubitId(0)]);
            for q in 0..n.min(3) {
                ch2.h(&[QubitId(q)]);
            }
            if n > 2 {
                ch2.cx(&[(QubitId(1), QubitId(2))]);
            }

            for zq in [0, n.min(2) - 1, n / 2, n - 1] {
                let ip_ref = ch1.inner_product(&ch2);
                let ip_z_ref = ch1.inner_product_z(&mut ch2, zq);
                let (ip_new, ip_z_new) = ch1.inner_product_pair(&ch2, zq);

                assert!(
                    (ip_ref - ip_new).norm() < 1e-10,
                    "n={n}, zq={zq}: inner_product mismatch: {ip_ref:.8} vs {ip_new:.8}"
                );
                assert!(
                    (ip_z_ref - ip_z_new).norm() < 1e-10,
                    "n={n}, zq={zq}: inner_product_z mismatch: {ip_z_ref:.8} vs {ip_z_new:.8}"
                );
            }
        }
    }

