    #[test]
    fn test_prob_z_zero_matches_statevec() {
        use crate::StateVec;

        type CircuitFn = Box<dyn Fn(&mut CHForm, &mut StateVec)>;

        // Several circuits, verify prob_z_zero matches StateVec probabilities.
        let circuits: Vec<(&str, usize, CircuitFn)> = vec![
            (
                "H-CX-S",
                2,
                Box::new(|ch: &mut CHForm, sv: &mut StateVec| {
                    ch.h(&qid(0)).cx(&[(QubitId(0), QubitId(1))]).sz(&qid(0));
                    sv.h(&qid(0)).cx(&[(QubitId(0), QubitId(1))]).sz(&qid(0));
                }),
            ),
            (
                "H-all-CX-chain",
                3,
                Box::new(|ch: &mut CHForm, sv: &mut StateVec| {
                    ch.h(&qid(0))
                        .h(&qid(1))
                        .h(&qid(2))
                        .cx(&[(QubitId(0), QubitId(1))])
                        .cx(&[(QubitId(1), QubitId(2))]);
                    sv.h(&qid(0))
                        .h(&qid(1))
                        .h(&qid(2))
                        .cx(&[(QubitId(0), QubitId(1))])
                        .cx(&[(QubitId(1), QubitId(2))]);
                }),
            ),
        ];

        for (name, nq, circuit) in &circuits {
            let mut ch = CHForm::new(*nq);
            let mut sv = StateVec::new(*nq);
            circuit(&mut ch, &mut sv);

            let sv_state = sv.state();
            for q in 0..*nq {
                let ch_prob = ch.prob_z_zero(q);
                let sv_prob: f64 = sv_state
                    .iter()
                    .enumerate()
                    .filter(|(x, _)| (x >> q) & 1 == 0)
                    .map(|(_, a)| a.norm_sqr())
                    .sum();
                assert!(
                    (ch_prob - sv_prob).abs() < EPS,
                    "{name}: Pr(q{q}=0) ch={ch_prob:.6}, sv={sv_prob:.6}"
                );
            }
        }
    }

    #[test]
    fn test_inner_product_cross_circuit() {
        // Verify inner products between states from different gate sequences.
        // <0|+> = 1/sqrt(2), <+|-> = 0, <+|+i> = (1-i)/2
        let ch_zero = CHForm::new(1);

        let mut ch_plus = CHForm::new(1);
        ch_plus.h(&qid(0));

        let mut ch_minus = CHForm::new(1);
        ch_minus.x(&qid(0)).h(&qid(0)); // X then H: H|1> = |->

        let mut ch_plus_i = CHForm::new(1);
        ch_plus_i.h(&qid(0)).sz(&qid(0)); // S|+> = (|0>+i|1>)/sqrt(2)

        // <0|+> = 1/sqrt(2)
        let ip = ch_zero.inner_product(&ch_plus);
        assert!(
            (ip.re - std::f64::consts::FRAC_1_SQRT_2).abs() < EPS && ip.im.abs() < EPS,
            "<0|+> should be 1/sqrt(2), got {ip:.6}"
        );

        // <+|-> = 0
        let ip = ch_plus.inner_product(&ch_minus);
        assert!(ip.norm() < EPS, "<+|-> should be 0, got {ip:.6}");

        // |<+|S|+>|^2 = |<+|(|0>+i|1>)/sqrt(2)|^2 = |(1+i)/2|^2 = 1/2
        let ip = ch_plus.inner_product(&ch_plus_i);
        assert!(
            (ip.norm_sqr() - 0.5).abs() < EPS,
            "|<+|S|+>|^2 should be 0.5, got {:.6}",
            ip.norm_sqr()
        );
    }

