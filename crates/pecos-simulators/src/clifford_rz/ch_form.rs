    const EPS: f64 = 1e-10;

    /// Compare state vectors up to global phase.
    /// Two state vectors are equivalent iff |<psi|phi>| = 1 (for normalized states).
    fn amplitudes_match_up_to_phase(ch: &CHForm, sv: &mut crate::StateVec, label: &str) {
        let n = ch.num_qubits();
        let ch_sv = ch.state_vector();
        let sv_state = sv.state();

        // Find a non-zero amplitude to determine the global phase ratio.
        let mut phase_ratio = None;
        for x in 0..(1 << n) {
            if sv_state[x].norm() > EPS && ch_sv[x].norm() > EPS {
                phase_ratio = Some(sv_state[x] / ch_sv[x]);
                break;
            }
        }

        if let Some(ratio) = phase_ratio {
            // All amplitudes should differ by the same global phase.
            for x in 0..(1 << n) {
                let ch_rotated = ch_sv[x] * ratio;
                let diff = (ch_rotated - sv_state[x]).norm();
                assert!(
                    diff < EPS,
                    "{label}: amplitude[{x}] mismatch (after phase correction): \
                     ch={:.6}, sv={:.6}, ratio={ratio:.6}, diff={diff:.2e}",
                    ch_sv[x],
                    sv_state[x]
                );
            }
        } else {
            // Both should be zero vectors
            for x in 0..(1 << n) {
                assert!(
                    ch_sv[x].norm() < EPS && sv_state[x].norm() < EPS,
                    "{label}: amplitude[{x}] should be zero"
                );
            }
        }
    }

