//! CH-form stabilizer simulator.
//!
//! Represents stabilizer states as `|psi> = omega * U_C * U_H * |s>` following
//! Bravyi et al. (arXiv:1808.00128). Tracks global phase, enabling inner products
//! between stabilizer states -- the key capability that standard stabilizer tableaux lack.
//!
//! The internal binary matrices F, G, M use the same dual row/column sparse representation
//! as [`SparseStab`](crate::SparseStab) for fast gate updates.
//!
//! # Gate update rules
//!
//! C-type gates (S, Z, CZ, CX) directly update F, G, M, gamma.
//! X gate requires commuting through `U_C` and `U_H`.
//! H gate requires the `update_s_vector` subroutine (most complex).
//!
//! # References
//!
//! - Bravyi, Browne, Calpin, Campbell, Gosset, Howard.
//!   "Simulation of quantum circuits by low-rank stabilizer decompositions."
//!   arXiv:1808.00128 (2019).

